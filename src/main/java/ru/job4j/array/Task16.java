package ru.job4j.array;

public class Task16 {
    public static void decreaseBy2(int number) {
        int a, b, c;
        a = number - 1;
        b = a - 1;
        c = b - 1;
        System.out.println(
                a + System.lineSeparator()
                        + b + System.lineSeparator()
                        + c);
    }
}
